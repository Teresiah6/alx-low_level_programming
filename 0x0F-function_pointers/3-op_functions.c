#include "3-calc.h"
/**
 * op_add - add func
 * @a: first int
 * @b: second int
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract func
 * @a: first int
 * @b: second int
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply func
 * @a: first int
 * @b: second int
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div -division func
 * @a: first int
 * @b: second int
 * Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus func
 * @a: first int
 * @b: second int
 * Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
