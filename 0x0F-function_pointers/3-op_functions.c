#include "3-calc.h"
/**
 * op_add - add func
 * op_sub - substract
 * op_mul - multiply
 * op_div - division
 * op_mod -modulus
 * @a: first int
 * @b: second int
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}
int op_sub(int a, int b)
{
	return (a - b);
}
int op_mul(int a, int b)
{
	return (a * b);
}
int op_div(int a, int b)
{
	return (a / b);
}
int op_mod(int a, int b)
{
	return (a % b); 
}
